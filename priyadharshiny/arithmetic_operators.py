a = 15
b = 4

# addition +
print("Addition: ", a + b)

# subtraction (-)
print("Subtraction: ", a - b)

# multiplication (*)
print("Multiplication: ", a * b)

# division (/)
print("Division: ", a / b)

# floor division (//)
print("Floor Division: ", a // b)

# module (%)
print("Modulo: ", a % b)

# exponentional (**)
print("Exponent: ", a ** b)