# equal
x = 10
print("x = ", x)

# +=
a = 10
a += 5
print("after += 5:", a)

# -=
b = 20
b -= 7
print("after -= 7:", b)

# *=
c = 6
c *= 4
print("after *= 3:", c)

# /=
d = 50
d /= 8
print("after /= 8:", d)

# %=
e = 29
e %= 5
print("after %= 5:", e)

# **=
f = 3
f **= 4
print("after **= 4:", f)

# //=
g = 50
g //= 7
print("after //= 7:", g)